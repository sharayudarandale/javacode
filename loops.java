public class loops {
    public static void main(String[] args) {
       

        // for loop
        for(int i=0;i<10;i++){
            System.out.println(i);
    }
            // while loop
            int x=0;
            while(x<10){
                System.out.println(x);
                x=x+1;
            }
            // do while loop
            int j=0;
           
            do{
                System.out.println(j);
                j=j+1;

            }while(j<11);

            int sum=0;
            int n=10;
            for(int i=0;i<=n;i++){
                sum =sum+i;
            }
            System.out.println(sum);
        }
    
    }

